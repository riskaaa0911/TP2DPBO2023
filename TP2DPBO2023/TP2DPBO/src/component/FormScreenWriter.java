    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        nama = nameWriter.getText();
        jumlah_drama = Integer.parseInt(jmlDrama.getText());
        if((nama != null) && (jumlah_drama != -1) && (imgHandler.getFile() != null)){
            Date dNow = new Date();
            SimpleDateFormat ft = new SimpleDateFormat("yyMMddhhmmssMs");
            this.img = ft.format(dNow)+"_"+imgHandler.getFile().getName();
            try{
                uploadImg(this.img);
                String sql = "INSERT INTO `tb_screenwriter` (`id`, `nama`, `jumlah_drama`, `img`) VALUES (null, '"+nama+"','"+jumlah_drama+"','"+img+"')";
                db.stm.execute(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil Ditambah", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                clearForm();
                this.mf.setItem();
                
                this.dispose();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Lengkapi Form", "Warning", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

