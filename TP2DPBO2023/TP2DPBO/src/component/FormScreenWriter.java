    public void setScreenwriter(){
        try{
            String sql = "SELECT * FROM tb_screenwriter WHERE id = '"+this.id+"'";
            ResultSet res = db.Query(sql);
            while(res.next()){
                this.id = res.getInt("id");
                this.nama = res.getString("nama");
                this.jumlah_drama = res.getInt("jumlah_drama");
                this.img = res.getString("img");
            }            
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }
    }
    
